// quiz

// What is the difference between var and let?
// Both are keywords used to declare variables, except let creates a block-scoped variable (only exists inside a code block) while variables are hoisted by var
// You can redeclare and reassign a variable with var, but cannot redeclare with let

// What is the difference between var and const?
// You can redeclare and reassign a variable with var, but cannot do either with const. Const also creates a block-scope variable.


// What is the difference between let and const?
// You can reassign with let, but you can not redeclare or reassign using the const keyword.


// What is hoisting?
// Hoisting refers to the process whereby the interpreter appears to move the declaration of functions, variables or classes to the top of their scope, prior to execution of the code.
